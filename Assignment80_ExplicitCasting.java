package javaAssignments;

//Assignment 80: Write a Java program to convert double into float datatype.

public class Assignment80_ExplicitCasting {
	
	public static void main(String[] args) {
		
		double Dvalue=10.5;
		
		float Fvalue=(float) Dvalue;        //Explicit Casting(Narrowing Casting)
		
		System.out.println(Fvalue);
		
		
		
		
	}
	
	

}
